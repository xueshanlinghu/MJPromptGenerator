        æ¸…ç©ºæ‰€æœ‰
      </n-button>
    </div>

    <div v-if="currentPrompt" class="prompt-info">
      <span class="info-item">
        <span class="info-label">æç¤ºè¯æ•°é‡:</span>
        <span class="info-value">{{ selectedCount }}</span>
