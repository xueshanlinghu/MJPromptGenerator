<template>
  <div class="final-prompt">
    <div v-if="autoGeneratedPrompt || editedPrompt" class="prompt-editor">
      <n-input
        v-model:value="editedPrompt"
        type="textarea"
        placeholder="é€‰æ‹©æç¤ºè¯å’Œå‚æ•°åå°†è‡ªåŠ¨ç”Ÿæˆï¼Œä¹Ÿå¯ä»¥åœ¨æ­¤ç›´æ¥ç¼–è¾‘..."
        :autosize="{ minRows: 5, maxRows: 8 }"
        @update:value="handleEdit"
      />
      <div v-if="isManuallyEdited" class="edit-hint">
        <span class="hint-icon">âœï¸</span>
        <span class="hint-text">å·²æ‰‹åŠ¨ç¼–è¾‘</span>
        <n-button text @click="handleReset" class="reset-link">
          é‡ç½®ä¸ºè‡ªåŠ¨ç”Ÿæˆ
        </n-button>
      </div>
    </div>

    <div v-else class="empty-state">
      <span class="empty-icon">âœ¨</span>
      <span class="empty-text">é€‰æ‹©æç¤ºè¯å’Œå‚æ•°åå°†åœ¨è¿™é‡Œæ˜¾ç¤º</span>
