        {{ copied ? 'å·²å¤åˆ¶!' : 'å¤åˆ¶æç¤ºè¯' }}
      </n-button>

      <n-button
        :disabled="!hasContent"
        @click="handleClear"
        secondary
      >
        <template #icon>
          <span>ğŸ—‘ï¸</span>
