<script setup lang="ts">
import { computed, ref, watch } from 'vue'
import { NButton, NInput } from 'naive-ui'
import { storeToRefs } from 'pinia'
import { usePromptStore } from '@/stores/promptStore'
import { useParameterStore } from '@/stores/parameterStore'
import { PromptBuilder } from '@/utils/promptBuilder'

const promptStore = usePromptStore()
const parameterStore = useParameterStore()

const { selectedPrompts } = storeToRefs(promptStore)
const { values } = storeToRefs(parameterStore)

const copied = ref(false)
const editedPrompt = ref('')
const isManuallyEdited = ref(false)

// è®¡ç®—å±æ€§ï¼šè‡ªåŠ¨ç”Ÿæˆçš„æç¤ºè¯
const autoGeneratedPrompt = computed(() => {
  if (selectedPrompts.value.length === 0) {
    return ''
  }

  // è·å–æç¤ºè¯æ–‡æœ¬åˆ—è¡¨ï¼ˆæŒ‰æ—¶é—´é¡ºåºï¼‰
  const promptTexts = selectedPrompts.value
    .sort((a, b) => a.timestamp - b.timestamp)
    .map(p => p.text)

  // ç»„åˆæç¤ºè¯å’Œå‚æ•°
  return PromptBuilder.build(promptTexts, values.value)
})

// å½“å‰ä½¿ç”¨çš„æç¤ºè¯ï¼ˆæ‰‹åŠ¨ç¼–è¾‘ä¼˜å…ˆï¼‰
const currentPrompt = computed(() => {
  return editedPrompt.value || autoGeneratedPrompt.value
})

// ç›‘å¬è‡ªåŠ¨ç”Ÿæˆçš„æç¤ºè¯å˜åŒ–
watch(autoGeneratedPrompt, (newVal) => {
  // åªæœ‰åœ¨æœªæ‰‹åŠ¨ç¼–è¾‘æ—¶æ‰è‡ªåŠ¨åŒæ­¥
  if (!isManuallyEdited.value) {
    editedPrompt.value = newVal
  }
}, { immediate: true })

// è®¡ç®—å±æ€§ï¼šé€‰ä¸­çš„æç¤ºè¯æ•°é‡
const selectedCount = computed(() => {
  return selectedPrompts.value.length
})

// è®¡ç®—å±æ€§ï¼šæ˜¯å¦æœ‰å†…å®¹
const hasContent = computed(() => {
  return selectedPrompts.value.length > 0 || Object.keys(values.value).length > 0
})

// å¤„ç†æ‰‹åŠ¨ç¼–è¾‘
const handleEdit = () => {
  isManuallyEdited.value = true
}

// é‡ç½®ä¸ºè‡ªåŠ¨ç”Ÿæˆ
const handleReset = () => {
  isManuallyEdited.value = false
  editedPrompt.value = autoGeneratedPrompt.value
}

// å¤åˆ¶åˆ°å‰ªè´´æ¿
const handleCopy = async () => {
  if (!currentPrompt.value) return

  try {
    await navigator.clipboard.writeText(currentPrompt.value)
    copied.value = true

    // 2ç§’åé‡ç½®çŠ¶æ€
    setTimeout(() => {
      copied.value = false
    }, 2000)
  } catch (error) {
    console.error('å¤åˆ¶å¤±è´¥:', error)
    // é™çº§æ–¹æ¡ˆï¼šä½¿ç”¨ä¼ ç»Ÿæ–¹æ³•
    const textarea = document.createElement('textarea')
    textarea.value = currentPrompt.value
    textarea.style.position = 'fixed'
    textarea.style.opacity = '0'
    document.body.appendChild(textarea)
    textarea.select()
    document.execCommand('copy')
    document.body.removeChild(textarea)

    copied.value = true
    setTimeout(() => {
      copied.value = false
    }, 2000)
  }
}

// æ¸…ç©ºæ‰€æœ‰é€‰é¡¹
const handleClear = () => {
  promptStore.clearAll()
  parameterStore.resetToDefaults()
  editedPrompt.value = ''
  isManuallyEdited.value = false
}
</script>

